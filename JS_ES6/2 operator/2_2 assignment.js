/*
-----------------------------------
Operator	Example	    Same As
-----------------------------------
=	        x = y	    x = y
+=	        x += y	    x = x + y
-=	        x -= y	    x = x - y
*=	        x *= y	    x = x * y
/=	        x /= y	    x = x / y
%=	        x %= y	    x = x % y
**=	        x **= y	    x = x ** y
-----------------------------------
*/

// Assignment
var x = 5;

// Addition Assignment
x += 2;
console.log(x);

// Subtraction Assignment
x -= 2;
console.log(x);

// Multiplication Assignment
x *= 2;
console.log(x);

// Division Assignment
x /= 2;
console.log(x);

// Modulus Assignment
x %= 2;
console.log(x);

// Exponentiation Assignment
x **= 2;
console.log(x);