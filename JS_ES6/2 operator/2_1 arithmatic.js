/*
----------------------------------------------
Operator	   Description
---------------------------------------------
+	           Addition
-	           Subtraction
*	           Multiplication
**	           Exponentiation (ES2016)
/	           Division
%	           Modulus (Division Remainder)
++	           Increment
--	           Decrement
----------------------------------------------
*/

// Addition
var x = 5;
var y = 2;
var z = x + y;
console.log(z);

// Subtraction
var x = 5;
var y = 2;
var z = x - y;
console.log(z);

// Multiplication
var x = 5;
var y = 2;
var z = x * y;
console.log(z);

// Division
var x = 5;
var y = 2;
var z = x / y;
console.log(z);

// Modulus
var x = 5;
var y = 2;
var z = x % y;
console.log(z);

// Increment
var x = 5;
x++;
console.log(x);

// Decrement
var x = 5;
x--;
console.log(x);

// Exponentiation
var x = 5;
var y = 2;
var z = x ** y;
console.log(z);