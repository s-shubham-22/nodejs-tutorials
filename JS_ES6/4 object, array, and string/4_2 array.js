/*
The Array object, as with arrays in 
other programming languages, enables storing 
a collection of multiple items under a single 
variable name, and has members for performing 
common array operations.
*/

// create an array
var array = [1, 2, 3, 4, 5];
console.log(array);

// create an array using new keyword
var array = new Array(1, 2, 3, 4, 5, 6);
console.log(array);

